$(document).ready(function(){
    $('#upper').click(function(){
        $('p').css('text-transform', 'uppercase')
    })
    $('#red').click(function(){
        $('p').css('color', 'red')
    })
    $('#blue').click(function(){
        $('p').css('color', 'blue')
    })
    $('#yellow').click(function(){
        $('p').css('color', 'yellow')
    })
})
